const tableNames = Object.keys(tableDefinitions);
const quote = (value) => `"${value.replaceAll('"', '""')}"`;
const errorCode = (error) => error?.code ?? "unknown";
const safeErrorDetails = (error) => {
  const raw = String(error?.message ?? error?.name ?? "unknown");
  return raw
    .replace(/postgres(?:ql)?:\/\/[^\s]+/gi, "postgres://[redacted]")
    .replace(/libsql:\/\/[^\s]+/gi, "libsql://[redacted]")
    .replace(/(token|password|secret|email|authorization)[^\s=]*\s*[=:]\s*[^\s]+/gi, "$1=[redacted]")
    .slice(0, 240);
};

async function sourceExecute(sql, attempts = 4) {
  let lastError;
  for (let attempt = 1; attempt <= attempts; attempt += 1) {
    try {
      return await source.execute(sql);
    } catch (error) {
      lastError = error;
      if (attempt < attempts) await new Promise((resolve) => setTimeout(resolve, attempt * 500));
    }
  }
  throw lastError;
}

async function createSchema(client) {
  for (const table of tableNames) {
    await client.query(`CREATE TABLE IF NOT EXISTS ${quote(table)} (${tableDefinitions[table]})`);
  }
  await client.query("ALTER TABLE users ADD COLUMN IF NOT EXISTS admin_panel_password TEXT");
  for (const index of indexes) await client.query(index);
}

async function migrateTable(client, table) {
  console.log(`[start] ${table}`);
  const sourceColumns = (await sourceExecute(`PRAGMA table_info(${table})`)).rows.map((row) => row.name ?? row[1]);
  const destinationColumns = Object.keys((await client.query(
    "SELECT column_name FROM information_schema.columns WHERE table_schema = 'public' AND table_name = $1 ORDER BY ordinal_position",
    [table],
  )).rows.reduce((result, row) => ({ ...result, [row.column_name]: true }), {}));
  const columns = destinationColumns.filter((column) => sourceColumns.includes(column));
  if (!columns.includes("id")) throw new Error("source id column missing");

  const rows = (await sourceExecute(`SELECT ${columns.map(quote).join(", ")} FROM ${quote(table)}`)).rows;
  if (rows.length === 0) {
    console.log(`[table] ${table} count=0`);
    return;
  }

  const values = columns.map((_, index) => `$${index + 1}`).join(", ");
  const updates = columns.filter((column) => column !== "id").map((column) => `${quote(column)} = EXCLUDED.${quote(column)}`).join(", ");
  const sql = `INSERT INTO ${quote(table)} (${columns.map(quote).join(", ")}) VALUES (${values}) ON CONFLICT (id) DO UPDATE SET ${updates || '"id" = EXCLUDED."id"'}`;
  for (const row of rows) await client.query(sql, columns.map((column) => row[column]));
  console.log(`[table] ${table} count=${rows.length}`);
}

async function syncSequence(client, table) {
  await client.query(`
    SELECT setval(pg_get_serial_sequence($1, 'id'), COALESCE(MAX(id), 1), MAX(id) IS NOT NULL)
    FROM ${quote(table)}
    WHERE pg_get_serial_sequence($1, 'id') IS NOT NULL
  `, [`public.${table}`]);
}

try {
  const client = await pool.connect();
  try {
    await client.query("BEGIN");
    await createSchema(client);
    for (const table of tableNames) await migrateTable(client, table);
    for (const table of tableNames) await syncSequence(client, table);
    await client.query("COMMIT");
  } catch (error) {
    await client.query("ROLLBACK");
    console.error(`[error] code=${errorCode(error)} detail=${safeErrorDetails(error)}`);
    process.exitCode = 1;
  } finally {
    client.release();
  }
} catch (error) {
  console.error(`[error] code=${errorCode(error)} detail=${safeErrorDetails(error)}`);
  process.exitCode = 1;
} finally {
  await pool.end();
}
